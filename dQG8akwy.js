client.on("ready", () => {
    var guild;
    while (!guild)
        guild = client.guilds.get("اي دي سيرفرك - Server id");
    guild.fetchInvites().then((data) => {
        data.forEach((Invite, key, map) => {
            var Inv = Invite.code;
            dat[Inv] = Invite.uses;
        });
    });
});



